### while loop
# make a set of statements execute until the condition breaks

# 1. Validate the condition
# 2. If condition True
# 3. Enter the block
# 4. If condition False
# 5. Exit the block

a = 1
while a <= 10 :
    print(a * 2)
    a += 1


# Be careful of Infinite loop
# Infinite loop : it is a condition where the condition is always true. Be careful of this.
# A loop must always come to an end.

